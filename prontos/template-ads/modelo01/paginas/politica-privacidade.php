            <h1>Politica de Privacidade</h1>
            
            <p><strong>Nesta Política de Privacidade, vamos explicar aos nossos visitantes e usuários, todas as formas e
                    meios que protegemos e tratamos os dados coletados em nossa plataforma.</strong></p>
            <p>Nós levamos extremamente a sério a proteção dos dados pessoais coletados e armazenados, e neste
                documento, vamos te explicar detalhadamente como e porque fazemos esta coleta, e como você pode pedir
                para retirar, alterar ou excluir seus dados de nossos servidores.</p>
            <p>Em caso de dúvidas de quaisquer tipos, você pode entrar em contato diretamente com nossa equipe pela aba
                de contato.</p>

            <h3 class="wp-block-heading"><strong>1 – Sobre a Origem dos seus dados Pessoais</strong></h3>
            <p>Nosso site pode coletar alguns dados pessoais seus para diferentes objetivos. <strong>Para coletarmos
                    estes dados, usamos as seguintes tecnologias</strong>:</p>
            <ul>
                <li><strong>Website: </strong>Dentro do nosso site você poderá preencher formulários, enviar comentários
                    e dúvidas, entrar em contato com nossa equipe ou se cadastrar na nossa plataforma, e seus dados
                    serão salvos para posterior atendimento.</li>
                <li><strong>Mensagens e comunicações:</strong> Quando você se comunica com nossa equipe, seja pelo
                    Whatsapp, por SMS, por E-mail ou quaisquer outros tipos de chat, alguns dados podem ficar salvos
                    para futura conferência e facilitar a comunicação com nossos clientes e usuários.</li>
                <li><strong>De Modo Offline:</strong> Alguns dados podem ser coletados offline, como, por exemplo, em
                    eventos e organizações, onde podemos coletar seu e-mail ou outros dados, para manter comunicação e
                    informações necessárias que você precisa ter conhecimento.</li>
                <li><strong>Dados de Terceiros</strong>: Algumas vezes, terceiros podem enviar para nossa empresa alguns
                    dados seus, como, por exemplo, dados recebidos por mídias sociais (Como Facebook, instagram, Tiktok
                    ou outras redes sociais), do governo ou intermediadoras de pagamentos.</li>
            </ul>
            <h3 class="wp-block-heading"><strong>2 – Como e porque coletamos seus dados:</strong></h3>
            <p><strong>É importante que você, nosso usuário ou visitante, saiba como e quais dados pessoais coletamos e
                    utilizamos quando você visita nosso site.</strong> Destacamos que sempre tentamos coletar o mínimo
                de dados possíveis para sua maior segurança, porém, alguns dados são essenciais para podermos entregar o
                melhor serviço possível e até para o funcionamento do site, conforme descrevemos abaixo:</p>
            <ul>
                <li><strong>Marketing:</strong> Quando você preenche formulários e se cadastra no site, você poderá
                    receber e-mails com promoções e novidades do site. Podemos coletar nome, e-mail e telefone.</li>
                <li><strong>Navegação ao Site:</strong> quando você navega pelo nosso site, coletamos automaticamente
                    dados como o seu endereço IP, tipo de navegador, páginas acessadas, tempo gasto em cada página e
                    outras informações.</li>
                <li><strong>Informações de contato: </strong>quando você entra em contato conosco, seja por e-mail,
                    telefone ou chat, coletamos as suas informações de contato, como o seu nome, e-mail, número de
                    telefone e a mensagem que você nos enviou.</li>
                <li>I<strong>nformações de uso dos serviços:</strong> quando você usa os nossos serviços, coletamos
                    dados sobre as suas atividades, como as pesquisas que você realiza, os vídeos que você assiste, as
                    páginas que você visita e as compras que você faz e quando você compartilha conosco fotos,
                    comentários, sugestões, perguntas, respostas, informações, textos, arquivos, gráficos, vídeos ou
                    outros materiais, coletamos as informações que você nos enviar.</li>
                <li><strong>Informações de localização: </strong>quando você usa os nossos serviços, coletamos dados de
                    localização precisos para fornecer os serviços que você solicitou. Por exemplo, quando você solicita
                    um serviço de entrega, coletamos seu endereço para enviar a entrega para o local certo.</li>
            </ul>
            <h3 class="wp-block-heading"><strong>3 – Sobre o uso de Cookies</strong></h3>
            <p><strong>Usamos cookies no nosso website e aplicativos para melhorar a sua experiência de navegação no
                    nosso website</strong>. Um cookie é um pequeno arquivo de texto que um site armazena no seu
                computador ou dispositivo móvel quando você visita o site.&nbsp;</p>
            <p>Os cookies nos permitem reconhecer o seu navegador e fornecer-lhe a melhor experiência ao navegar no
                nosso site. Além disso, os cookies nos ajudam a compreender quais seções do site são mais interessantes
                para você e quais conteúdos podem ser recomendados para você, site com o <a
                    href="https://www.google.com.br/" target="_blank" rel="noopener">google</a>, bing ou <a
                    href="https://www.gpabrasil.com.br/" target="_blank" rel="noopener">https://gpabrasil.com.br/</a>,
                utilizam cookies, e ao visitar cada um, você deve verificar a politica referente a eles.</p>
            <p>Você pode a qualquer momento bloquear os cookies no seu navegador, ou limpar o cache para retirar os
                mesmos.</p>
            <p>É importante destacar que tentamos minimizar o uso de cookies não essenciais, mas utilizamos os
                necessários para entregar um melhor serviço, conforme a previsão de legítimo interesse na LGPD. Você
                pode a qualquer momento desativar os mesmos em seu navegador.</p>
            <h3 class="wp-block-heading"><strong>4 – Com quem compartilhamos os seus dados pessoais:</strong></h3>
            <p><strong>Prestadores de Serviços: </strong>compartilhamos os seus dados pessoais com os nossos prestadores
                de serviços, que prestam serviços de marketing, análise de dados, pagamentos, envio de e-mails e outros
                serviços para nós. Esses prestadores de serviços só podem usar os seus dados pessoais para realizar
                esses serviços para nós e conforme as nossas instruções.</p>
            <p><strong>Anunciantes e Parceiros Comerciais:</strong> também é possível que compartilhemos os seus dados
                pessoais com empresas que realizem anuncio, ou parceiros comerciais. No entanto, só compartilhamos os
                seus dados pessoais conforme as suas instruções.</p>
            <p><strong>Comunidades Online: </strong>quando você se junta a uma das nossas comunidades online, os seus
                dados pessoais podem ser vistos por outros membros dessa comunidade. </p>
            <p><strong>Investidores:</strong> podemos compartilhar os seus dados pessoais com os nossos investidores. No
                entanto, só compartilhamos os seus dados pessoais conforme as suas instruções.</p>
            <p><strong>Filiais e Subsidiárias:</strong> podemos compartilhar os seus dados pessoais com as nossas
                filiais e subsidiárias. No entanto, só compartilhamos os seus dados pessoais conforme as suas
                instruções.</p>
            <p><strong>Autoridades Governamentais e outras Partes:</strong> às vezes, podemos ser obrigados
                judicialmente a liberar ou divulgar os seus dados às autoridades governamentais, ou outras partes,
                conforme exigido por lei. Ou por outros motivos, como:</p>
            <p>(i) Cumprir uma obrigação legal; </p>
            <p>(ii) Proteger os direitos, a propriedade ou a segurança da Página; </p>
            <p>(iii) Evitar um dano real ou potencial aos bens, ou à segurança das pessoas;</p>
            <p>(iv) De outra forma, conforme permitido por lei.</p>
            <h3 class="wp-block-heading"><strong>5- Sobre seus Direitos em relação aos Dados coletados</strong></h3>
            <p>O presente texto tem por objetivo esclarecer os nossos leitores e visitantes sobre quais são os seus
                direitos em relação aos dados pessoais.</p>
            <p><strong>De acordo com a <a href="http://www.planalto.gov.br/ccivil_03/_ato2015-2018/2018/lei/l13709.htm"
                        target="_blank" rel="noopener">Lei Geral de Proteção de Dados Pessoais</a> (LGPD), toda pessoa
                    tem o direito à informação, à transparência, à liberdade e à autonomia na tomada de
                    decisões.</strong></p>
            <p>Com relação aos dados pessoais, a pessoa tem o direito ao acesso, à correção, à eliminação, à
                portabilidade, à limitação do tratamento e à oposição.</p>
            <p>A pessoa também tem o direito de negas a qualquer momento que seus dados pessoais sejam utilizados para
                fins de marketing e ao recebimento de comunicações comerciais.</p>
            <p>Para exercer os seus direitos, basta entrar em contato com a nossa equipe.</p>
            <p>Esclarecemos que os dados pessoais são tratados de forma confidencial e que tomamos todas e quaisquer
                medidas de segurança necessárias para garantir a proteção dos dados.</p>
            <h3 class="wp-block-heading"><strong>6 – Sobre a segurança que aplicamos no tratamento dos seus
                    dados</strong></h3>
            <p>A segurança é um dos pilares fundamentais da nossa política de privacidade.</p>
            <p>Todos os dados pessoais que você fornecer ao site serão tratados com total confidencialidade e segurança,
                de acordo com as normas legais aplicáveis.</p>
            <p><strong>Para garantir a proteção dos seus dados pessoais, o site utiliza diversas medidas de segurança,
                    como por exemplo:</strong></p>
            <ul>
                <li>Criptografia SSL (Secure Socket Layer);</li>
                <li>Firewalls;</li>
                <li>Sistemas de detecção de invasões;</li>
                <li>Monitoramento constante das redes;</li>
                <li>Uso de senhas seguras;</li>
                <li>Verificação periódica da segurança dos sistemas.</li>
            </ul>
            <p>Todas as informações pessoais são armazenadas em servidores seguros, protegidos contra acessos não
                autorizados, utilizando os mais modernos e avançados sistemas de segurança.</p>
            <p>Nossa equipe de segurança está constantemente monitorando os sistemas e atualizando as medidas de
                proteção, para garantir que seus dados estejam sempre seguros.</p>
            <p>Caso você tenha alguma dúvida ou preocupação em relação à segurança dos seus dados pessoais, entre em
                contato conosco.</p>
            <h3 class="wp-block-heading"><strong>7 – Pedido de modificação, remoção ou alteração</strong></h3>
            <p><strong>Entendemos que você possa, eventualmente, querer modificar, remover ou alterar seus dados
                    pessoais. </strong></p>
            <p>Se você deseja fazer uma alteração, uma remoção ou uma modificação, entre em contato conosco e nós
                faremos o possível para atender à sua solicitação. No entanto, tenha em mente que, em alguns casos, pode
                não ser possível atender à sua solicitação, especialmente se for necessária para cumprir nossas
                obrigações legais ou contratuais.</p>
            <p>Além disso, lembre-se de que, se você solicitar a remoção de seus dados pessoais do nossos banco de
                dados, isso pode afetar o uso de nosso site e serviços. Se você não puder fazer uma alteração ou uma
                modificação, ou se desejar excluir seus dados pessoais,<strong> entre em contato conosco para podermos
                    discutir outras opções, clicando na aba contato.</strong></p>

